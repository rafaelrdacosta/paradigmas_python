# Exemplo de código em Python:
# Amarração de um inteiro a uma variável
idade = 30
print('Idade:', idade)
print(type(idade))

# Amarração de uma string a mesma variável
idade = 'trinta'
print('Idade em palavras:', idade)
print(type(idade))

# Amarração de um float a uma nova variável
altura = 1.75
print('Altura:', altura)
print(type(idade))