# Definir a função chamada imprimir_dados
def imprimir_dados(numero, texto):
    # Dentro da função, imprimir o número e a string
    print('Número:', numero)
    print('Texto:', texto)


# Fornecer dois valores para a função
numero_fornecido = 60
texto_fornecido =  'Olá, mundo!'

# Chamar a função com valores fornecidos
imprimir_dados(numero_fornecido, texto_fornecido)